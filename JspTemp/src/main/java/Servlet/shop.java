package Servlet;

public class shop {
		String name;
		String price;
		String num;
		public shop(String name,String price,String num){
			this.name = name;
			this.price = price;
			this.num = num;
		}
		public String getName() {
			return name;
		}
		public String getPrice() {
			return price;
		}
		public String getNum() {
			return num;
		}

}
